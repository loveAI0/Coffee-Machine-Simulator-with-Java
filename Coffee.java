class Coffee {
    final String name;
    final int waterNeeded;
    final int milkNeeded;
    final int beansNeeded;
    final int price;

    Coffee(String name, int water, int milk, int beans, int price) {
        this.name = name;
        this.waterNeeded = water;
        this.milkNeeded = milk;
        this.beansNeeded = beans;
        this.price = price;
    }
}
